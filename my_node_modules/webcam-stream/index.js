var ffmpeg = require('fluent-ffmpeg');
var proc = ffmpeg('/dev/video0')
                //.format('h264')
                .inputOptions([
                    '-f video4linux2',
                    '-r 30',
                    '-s 640x480'
                ])
                .outputOptions([
                    '-f mpeg1video',
                    '-b:v 800k',
                    'http://localhost:7002/640/480/'
                ])
                //.output('rtsp://localhost:7002/live.sdp')
                .on('end',function(msg){
                    console.log("finish ffmpeg command " + msg);
                })
                .on('err',function(err){
                    console.log("error found " + err);
                });
